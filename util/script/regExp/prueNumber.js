// 常用整数，小数 - 正则规则
export default {
  // [0,+∞]，但不超过 10 位小数的数，包含整数
  tenFloatNumReg: /^((([1-9]\d+)|\d)(\.\d{1,10})?)$/,

  // (0,100)，不超过 2 位小数的数
  inHundredTowNumReg: /(?!^0\.?0{0,2}$)^[0-9][0-9]?(\.[0-9]{1,2})?$/,

  // [0,100]，不超过 2 位小数的数
  zeroToHundredTowNumReg: /^(100|100.0|100.00|[1-9]?\d(\.\d?\d?)?)$/
};
